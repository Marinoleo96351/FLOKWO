// Uma variável é uma "caixa com nome" que guarda um valor.

// const = valor que NÃO vai mudar depois de criado

const nomeDoProjeto = "FLOKWO";

// let = valor que PODE mudar ao longo do tempo
let tarefasConcluidas = 0 ;

// Aqui mudamos o valor de uma variável 'let' — isso é permitido
tarefasConcluidas = 1;
tarefasConcluidas = 2;

// console.log é como você "vê" o que está acontecendo no Node.
// Pensa nele como um "mostre isso na tela pra mim".
console.log("Projeto:", nomeDoProjeto);
console.log("Tarefas concluidas:", tarefasConcluidas);